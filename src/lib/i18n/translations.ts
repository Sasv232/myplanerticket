export type TranslationKey = keyof typeof translations.ru;

export function t(key: TranslationKey, lang: Lang = "ru"): string {
  const val = translations[lang]?.[key] ?? translations.ru[key];
  if (typeof val === "function") return key;
  return val as string;
}

export function tf(key: TranslationKey, lang: Lang = "ru"): (...args: any[]) => string {
  const val = translations[lang]?.[key] ?? translations.ru[key];
  if (typeof val === "function") return val as (...args: any[]) => string;
  return () => val as string;
}
